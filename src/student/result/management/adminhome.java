    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         String course =(String)jComboBox1.getSelectedItem();
         String branch=(String)jComboBox2.getSelectedItem();
         String rollno=jTextField1.getText();
         String name=jTextField2.getText();
         String gender=(String)jComboBox3.getSelectedItem();
         String fathername=jTextField3.getText();
         String phone= jTextField4.getText();
      
         try{
             Class.forName("com.mysql.jdbc.Driver");
             Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Java@123");
             Statement st=(Statement)con.createStatement();
             st.executeUpdate("insert into student(course,branch,rollno,name,gender,fathername,phone) values('"+course+"','"+branch+"','"+rollno+"','"+name+"','"+gender+"','"+fathername+"','"+phone+"')");
             JOptionPane.showMessageDialog(null,"Successfully Updated");
             setVisible(false);
             new adminhome().setVisible(true);
             st.close();
             
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
             setVisible(false);
             new adminhome().setVisible(true);
         }
               
    }//GEN-LAST:event_jButton6ActionPerformed

